    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        String peso = txtPeso.getText(); 
        Float pesoFloat = Float.valueOf(peso);
        String altura = txtAltura.getText(); 
        Float alturaFloat = Float.valueOf(altura);
        Float imc = calcularIMC(pesoFloat, alturaFloat);
        
        String resultado = String.valueOf(imc);
        String textResultado = mostrarTextoResultado(resultado);
        
        txtResultado.setText(resultado + " -- " + textResultado);
        
        
                
        

    }//GEN-LAST:event_btnCalcularActionPerformed

    static float calcularIMC(float peso, float altura){
       return peso /(altura*altura);
    }
    
    static String mostrarTextoResultado(String resultado){
        Float resultadoFloat = Float.valueOf(resultado);
        if (resultadoFloat < 17) {
            return "Muito abaixo do peso";
        } else if (resultadoFloat >= 17 && resultadoFloat <= 18.49) {
            return "Abaixo do peso";
        } else if (resultadoFloat >= 18.5 && resultadoFloat <= 24.99) {
            return "Peso normal";
        } else if (resultadoFloat >= 25 && resultadoFloat <= 29.99) {
            return "Acima do peso";
        } else if (resultadoFloat >= 30 && resultadoFloat <= 34.99) {
            return "Obesidade I";
        } else if (resultadoFloat >= 35 && resultadoFloat <= 39.99) {
            return "Obesidade II (Severa)";
        } else {
            return "Obesidade III (MÃ³bida)";
        }
    }
